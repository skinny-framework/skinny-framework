package skinny.assets

import org.scalatest.flatspec.AnyFlatSpec
import org.scalatest.matchers.should.Matchers

class CoffeeScriptCompilerSpec extends AnyFlatSpec with Matchers {

  behavior of "CoffeeScriptCompiler"

  it should "compile code" in {
    val compiler = CoffeeScriptCompiler()
    val js = compiler.compile(
      "person.coffee",
      """class Person
        |  constructor: (name, email) ->
        |    @name = name
        |    @email = email
        |  name: 'Anonymous'
        |  email: null
        |  sayHello: -> console.log "My name is #{@name}!"
        |
        |bob = new Person('Bob')
        |bob.sayHello()
      """.stripMargin
    )

    js.replaceFirst("^// Generated by CoffeeScript .+\n", "") should equal(
      """(function() {
        |  var Person, bob;
        |
        |  Person = (function() {
        |    function Person(name, email) {
        |      this.name = name;
        |      this.email = email;
        |    }
        |
        |    Person.prototype.name = 'Anonymous';
        |
        |    Person.prototype.email = null;
        |
        |    Person.prototype.sayHello = function() {
        |      return console.log("My name is " + this.name + "!");
        |    };
        |
        |    return Person;
        |
        |  })();
        |
        |  bob = new Person('Bob');
        |
        |  bob.sayHello();
        |
        |}).call(this);
        |""".stripMargin
    )
  }

}
